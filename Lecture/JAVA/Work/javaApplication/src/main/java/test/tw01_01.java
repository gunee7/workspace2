package test;

public class tw01_01 {
    public static void main(String[] args) {
        
        //hello, world 출력하시오
        System.out.println("Hello, World");
        
        //hello,
        //world 출력하시오
        
        // ? 자리에 "줄바꿈 특수 문자"를 넣으시오
        System.out.println("Hello,? World");
        System.out.println("Hello,\n World");
        
        //? 자리에 "탭 특수 문자"를 넣으시오
        System.out.println("Hello,? World");
        System.out.println("Hello,\t World");
        
    }
}
