package java02;

public class jv02_중간점검 {
    public static void main(String[] args) {
        
        // 다음의 각 변수의 값을 적어보라.
        
        int x = 1;
        int y = 1;
        int a = ++x*2;   // a의 값은   4
        int b = y++*2;   // b의 값은   2
        
        System.out.println(a);
        System.out.println(b);
        
    }
}
